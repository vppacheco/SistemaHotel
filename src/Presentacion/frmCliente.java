    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txtnombre.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar un nombre para el cliente ");
            txtnombre.requestFocus();
            return;

        }

        if (txtapellidop.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar una un apellido de cleinte");
            txtapellidop.requestFocus();
            return;

        }

        if (txtapellidom.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar una un apellido de cleinte");
            txtapellidom.requestFocus();
            return;

        }

        if (txtnumerodocumento.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar un num de documento del cliente");
            txtnumerodocumento.requestFocus();
            return;

        }

        if (txtcodigocliente.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Debes Ingresar una codigo de cliente");
            txtcodigocliente.requestFocus();
            return;

        }

        vcliente dts = new vcliente();
        fcliente func = new fcliente();

        dts.setNombre(txtnombre.getText());
        dts.setApaterno(txtapellidop.getText());
        dts.setAmaterno(txtapellidom.getText());

        int seleccionado = cbotipodocumento.getSelectedIndex();
        dts.setTipo_documento((String) cbotipodocumento.getItemAt(seleccionado));

        dts.setNum_documento(txtnumerodocumento.getText());
        dts.setDireccion(txtdireccion.getText());

        dts.setTelefono(txttelefono.getText());
        dts.setEmail(txtemail.getText());
        dts.setCodigo_cliente(txtcodigocliente.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El cliente fue insertada correctamente");
                mostrar("");
                inhabilitar();

            }
        } else if (accion.equals("editar")) {

            dts.setIdpersona(Integer.parseInt(txtidpersona.getText()));

            if (func.editar(dts)) {
                JOptionPane.showConfirmDialog(rootPane, "El Cliente fue editada correctamente");
                mostrar("");
                inhabilitar();

            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

